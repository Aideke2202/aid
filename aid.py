import streamlit as st
import os
import json

# 1. Настройка внешнего вида страницы
st.set_page_config(page_title="Пустой Шаблон", page_icon="💼", layout="wide")

st.title("💼 Название вашего проекта")
st.write("Описание того, что делает это веб-приложение.")

